-- Profil Sayfası ve Premium Özellikler İçin Yeni Tablolar

-- 1. Favori Biletler Tablosu
CREATE TABLE IF NOT EXISTS public.user_favorites (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  bilet_id BIGINT REFERENCES public.biletler(id) ON DELETE CASCADE NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, bilet_id) -- Bir kullanıcı aynı bileti iki kez favorileyemez
);

-- 2. Kaydedilmiş AI Seyahat Planları Tablosu
CREATE TABLE IF NOT EXISTS public.user_trips (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  title TEXT NOT NULL,
  destination TEXT NOT NULL,
  trip_data JSONB NOT NULL, -- AI'ın ürettiği veriyi burada tutacağız
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Row Level Security (RLS) Güvenlik Ayarları
ALTER TABLE public.user_favorites ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.user_trips ENABLE ROW LEVEL SECURITY;

-- Kullanıcılar sadece kendi favorilerini görebilir, silebilir ve ekleyebilir
CREATE POLICY "Kullanıcılar kendi favorilerini yönetebilir" 
ON public.user_favorites 
FOR ALL 
USING (auth.uid() = user_id) 
WITH CHECK (auth.uid() = user_id);

-- Kullanıcılar sadece kendi seyahat planlarını görebilir ve yönetebilir
CREATE POLICY "Kullanıcılar kendi planlarını yönetebilir" 
ON public.user_trips 
FOR ALL 
USING (auth.uid() = user_id) 
WITH CHECK (auth.uid() = user_id);

-- Doğrulanmış Gezgin Sistemi Tablosu
CREATE TABLE IF NOT EXISTS public.travel_verifications (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  country_code TEXT NOT NULL,
  city_slug TEXT,
  verification_type TEXT NOT NULL, -- flight_ticket, boarding_pass, hotel_reservation, vs.
  verification_status TEXT DEFAULT 'pending', -- pending, approved, rejected, expired
  verified_at TIMESTAMPTZ,
  rejected_reason TEXT,
  proof_deleted_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE public.travel_verifications ENABLE ROW LEVEL SECURITY;

-- Sadece kendi verisini görebilir
CREATE POLICY "Kullanıcılar kendi doğrulama kayıtlarını görebilir" 
ON public.travel_verifications 
FOR SELECT 
USING (auth.uid() = user_id);

-- Sadece kendisi için ekleyebilir
CREATE POLICY "Kullanıcılar kendi doğrulama taleplerini oluşturabilir" 
ON public.travel_verifications 
FOR INSERT 
WITH CHECK (auth.uid() = user_id);

-- KVKK ve Veri Silme Talepleri Tablosu
