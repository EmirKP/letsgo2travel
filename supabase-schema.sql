  request_type TEXT NOT NULL, -- delete_data, withdraw_consent, account_deletion
  status TEXT DEFAULT 'pending', -- pending, processed, rejected
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
