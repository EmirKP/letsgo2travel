-- Letsgo2Travel Supabase Schema Definition
-- Supabase SQL Editor içinde çalıştırılabilir.

CREATE TABLE IF NOT EXISTS public.biletler (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug TEXT UNIQUE NOT NULL,
  title TEXT NOT NULL,
  origin TEXT NOT NULL,
  destination TEXT NOT NULL,
  origin_code VARCHAR(10) NOT NULL,
  destination_code VARCHAR(10) NOT NULL,
  price INT NOT NULL,
  currency VARCHAR(10) DEFAULT 'TRY',
  airline TEXT,
  travel_period TEXT,
  trip_type TEXT DEFAULT 'Gidiş dönüş',
  visa_type TEXT NOT NULL DEFAULT 'vizesiz',
  region TEXT NOT NULL DEFAULT 'Genel',
  image_url TEXT,
  affiliate_url TEXT NOT NULL,
  active BOOLEAN DEFAULT true,
  clicks INT DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.blogs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug TEXT UNIQUE NOT NULL,
  title TEXT NOT NULL,
  excerpt TEXT NOT NULL,
  content TEXT NOT NULL,
  category TEXT NOT NULL,
  read_time VARCHAR(20) NOT NULL,
  image_url TEXT,
  author VARCHAR(100) DEFAULT 'Letsgo2Travel',
  published_at TIMESTAMPTZ DEFAULT NOW(),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.country_guides (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug TEXT UNIQUE NOT NULL,
  country_code VARCHAR(10) NOT NULL,
  country_name TEXT NOT NULL,
  continent TEXT NOT NULL,
  region TEXT NOT NULL,
  emoji VARCHAR(10),
  visa_status VARCHAR(50) NOT NULL,
  visa_note TEXT,
  avg_flight_price INT NOT NULL DEFAULT 0,
  flight_duration VARCHAR(50),
  best_months VARCHAR(100),
  airport_code VARCHAR(10),
  is_popular BOOLEAN DEFAULT false,
  hero_image_url TEXT,
  content_markdown TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.videos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  youtube_id VARCHAR(50) UNIQUE NOT NULL,
  title TEXT NOT NULL,
  description TEXT,
  category TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.subscribers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email VARCHAR(255) UNIQUE NOT NULL,
  is_active BOOLEAN DEFAULT true,
  subscribed_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE public.biletler ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.blogs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.country_guides ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.videos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.subscribers ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "biletler public select" ON public.biletler;
DROP POLICY IF EXISTS "blogs public select" ON public.blogs;
DROP POLICY IF EXISTS "country guides public select" ON public.country_guides;
DROP POLICY IF EXISTS "videos public select" ON public.videos;
DROP POLICY IF EXISTS "subscribers insert" ON public.subscribers;

CREATE POLICY "biletler public select" ON public.biletler FOR SELECT USING (active = true);
CREATE POLICY "blogs public select" ON public.blogs FOR SELECT USING (true);
CREATE POLICY "country guides public select" ON public.country_guides FOR SELECT USING (true);
CREATE POLICY "videos public select" ON public.videos FOR SELECT USING (true);
CREATE POLICY "subscribers insert" ON public.subscribers FOR INSERT WITH CHECK (true);

CREATE OR REPLACE FUNCTION public.increment_deal_click(deal_id BIGINT)
RETURNS void AS $$
BEGIN
  UPDATE public.biletler
  SET clicks = COALESCE(clicks, 0) + 1,
      updated_at = NOW()
  WHERE id = deal_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

